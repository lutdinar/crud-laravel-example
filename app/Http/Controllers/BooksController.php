     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        // handling validation
        $request->validate([
            'judul' => 'required',
            'penulis' => 'required',
            'stok' => 'required',
            'deskripsi' => 'required',
        ]);

        Book::create([
            'judul' => $request->judul,
            'penulis' => $request->penulis,
            'stok' => $request->stok,
            'deskripsi' => $request->deskripsi,
        ]);

        // or

        // using fiilable from model access
        // Book::create($request->all());

        return redirect('/books')->with('status', 'New book created successfully');
    }

